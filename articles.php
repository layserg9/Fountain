                              защитные фильтры насосов с маленькими отверстиями. Желательно струи диаметром менее 8 мм
                              вообще не применять в городских общественных фонтанах.</p>
                           <p>Струйные насадки диаметром от 8 до 20 мм обычно используют группами для формирования
