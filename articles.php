                           <p> <strong> Пассивная безопасность фонтана.</strong></p>
                           <p>Помимо высокого напряжения в фонтане есть ещё много других опасностей:</p>
                           <ul>
                              <li>- глубина фонтана</li>
                              <li>- высота борта фонтана над тротуаром</li>
                              <li>- высота борта над уровнем воды</li>
                              <li>- скользкий тротуар вокруг фонтана </li>
                              <li>- скользкий борт фонтана</li>
                              <li>- острые грани облицовки фонтана</li>
                              <li>- опасная поверхность скульптур или элементов декора фонтана</li>
