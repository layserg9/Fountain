                              <li>- защитные фильтры</li>
                              <li>- подводные кабели</li>
                              <li>- скользкое, неровное или наклонное дно фонтана</li>
                           </ul>
                           <p>Как видите, опасностей в фонтане гораздо больше и устранить их сложнее, чем сделать
                              безопасным электрический ток в подводных насосах.</p>
                           <p><strong>Что и как нужно делать</strong></p>
                           <ul>
                              <li>- уровень воды фонтана должен быть минимальным для нормальной работы оборудования и
                                 эстетического восприятия. Нормальный достаточный уровень это примерно 300...500 мм
                              </li>
                              <li>- высота борта фонтана над тротуаром от 400 до 600 мм
                              </li>
                              <li>- высота борта над уровнем воды 100…200 мм</li>
                              <li>- струи не выше норматива</li>
                              <li>- тротуар вокруг фонтана из не скользящих материалов</li>
                              <li>-борт фонтана желательно из не полированного гранита</li>
                              <li>-не допускать острые грани облицовки фонтана, а также скользкое, неровное или
                                 наклонное дно фонтана</li>
                              <li>- использовать предупреждающие таблички</li>
                              <li>-опасные детали скульптур или элементов декора фонтана делаем на высоте выше
                                 человеческого роста или не делаем вообще</li>
                              <li>-исключить вероятность падения скульптур или деталей фонтана</li>
                              <li>-фонтанное оборудование размещаем дальше от борта ближе к центру</li>
                              <li>-торчащие вверх тонкие острые фонтанные насадки, заменяем толстыми, с сочными струями.
                                 Одна сочная струя эффектнее, чем пару десятков тоненьких струек. Меньше струй в
                                 фонтане, меньше, светильников и кабелей, меньше распределительных трубопроводов, меньше
                                 насосов, меньше размер насосной станции и тд. Соответственно и опасностей станет меньше
                              </li>
                              <li>-винты и крепления подводного освещения делаем гладкими и обтекаемыми</li>
                              <li>-подводные насосы по возможности объединяем в моноблочные фонтанные агрегаты для
                                 компактного размещения</li>
                              <li>-распределительные трубопроводы желательно проложить под дном фонтана</li>
                              <li>-защитные фильтры делаем гладкими и обтекаемыми
                              </li>
                              <li>-подводные кабели укладываем в пластиковые трубы под дном фонтана или в канальное
