                           <p>Если требуется более насыщенная водная картина, применяют пенообразующие насадки типа
                              «Пенный столб» (названия у всех свои, но принцип работы тот же). В насадке поток воды
                              разделяется на несколько струй, струи пропускаются через диффузор, где смешиваются с
                              атмосферным воздухом и вылетают из насадки одним ровным сильно вспененным потоком. Это
                              самый эффектный тип струй, может работать как вертикально, так и с наклоном. Прекрасно
                              обеспечивает аэрацию водоёма. Качество струи не зависит от колебаний уровня воды в
                              фонтане, но при больших высотах струи поток уже фрагментирован и не стабилен.</p>
                           <p>Если требуется создать насыщенную и динамичную водную картину при ограниченном бюджете, и
