                              преобразователи. Рядом с этим великолепием стоят огромные консольные насосы допотопных
                              конструкций с диким уровнем шума и вибраций. Переплетение труб, задвижек, клапанов. И
                              главное место занимает стол оператора. На столе расположены 2, 3 или даже 5 мониторов. На
                              каких-то мониторах открыты окна странных программ, на других мониторах трансляция с
                              наружных камер видеонаблюдения, направленных на фонтан. Оператору же нужно видеть, что он
                              там включает, из подземелья не видать ничего.</p>
                           <p><strong>Распределённая система управления фонтана</strong>выглядит иначе. Обычно
                              центральный блок управления цветомузыкальных фонтанов размером с небольшой чемодан, а
                              центральный блок управления светодинамических фонтанов размером с очень маленький чемодан.
                              Просто для светомузыкального фонтана требуется установка в шкаф управления ноутбука, а для
                              светодинамического фонтана не требуется. Центральный блок управления фонтана по
                              единственному двужильному экранированному проводу передаёт кодированные команды на все
                              исполнительные компактные блоки распределённого управления фонтана, которые располагаются
                              непосредственно возле управляемых насосов или светильников в ванне фонтана. Исполнительные
                              блоки декодируют сигнал и исполняют полученные команды, управляя насосами и светильниками.
                              Техническое подполье не требуется, километры кабелей и трубопроводов не требуются. А как
                              же рабочее место оператора? Рабочее место оператора не нужно. Современные программы имееют
                              функции 3D визуализации. Оператор может лежать на шезлонге и писать программу управления
                              фонтана, а потом просто перегоняет программу через интернет в центральный блок управления.
                              После этого оператор фонтану не нужен, всё работает автоматически. Если уж очень нужно
                              видеть живой фонтан, поставим вебкамеру с прямой трансляцией в сеть. </p>
                           <p>Может быть распределённые системы управления какая-то новинка и об этом мало кто знает?
                              Это не так. Уже в 80-х годах прошлого века, с широким распространением компьютеров в
                              промышленности, был разработан и принят стандартный протокол кодирования и обмена данными
                              по паре проводов RS485. С помощью него решалась задача управления с центрального
                              процессора всеми отдалёнными исполнительными устройствами и приборами по единственному
                              кабелю. Ведь большие производственные и технологические линии имели протяжённость в сотни
                              метров, а иногда и тысячи. За несколько десятков лет было разработано огромное количество
                              приборов и программ под этот протокол. Приборы есть в уличном исполнении, в подводном
                              исполнении и даже во взрывозащите. Программы написаны так хорошо, что даже пятилетний
                              ребёнок может ими пользоваться. На этих приборах и программах строятся системы управления
                              роботами и театральными сценами, и конечно, иностранные производители различных водных шоу
                              тоже работают исключительно с этим стандартом.</p>
                           <p>Зачем, при таких преимуществах распределённой системы управления фонтана перед
                              централизованной, фонтанные "специалисты" применяют древнюю технологию? В чём причина?
                              Причина первая: пробел в образовании, усиленный ленью. Зачем работать когда можно не
                              работать, всё и так продаётся. Причина вторая и главная: несколько маленьких коробочек с
                              маленькими насосами гораздо сложнее продать за 10 миллионов рублей, чем подземную
                              мега-фабрику с сотнями моргающих лампочек и большими мониторами за 25 миллионов рублей.
                              Заказчик не специалист, а чиновник. Он оценивает возможности системы исходя из её
                              размеров. По его логике маленький чемодан не может стоить дорого, потому что он слишком
                              маленький. А уж огромный железный шкаф, да ещё и с лампочкой, определённо должен стоить
                              очень дорого. А потом ещё местное телевидение приедет, надо будет что-то показать, чтобы
                              горожанам предъявить куда ушёл весь бюджет на благоустройство. А тут есть что предъявить,
                              гудящая мега-фабрика в недрах земли, а маленький чемоданчик не солидно по телевизору
                              показывать, чемодана чтоли не видали.</p>
