                              трубопроводов подачи и возврата циркулирующей воды, не нужно тратить энергию на трение
                              воды о стенки этих трубопроводов, а потери там просто неприличные. Быстро запустить
                              большой насос и разогнать объём воды в большой трубе, что обычно требуется в
